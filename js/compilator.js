var splitOnce = function(str, delim) {
    var components = str.split(delim);
    var result = [components.shift()];
    if(components.length) {
        result.push(components.join(delim));
    }
    return result;
};
function cleanSVG(string, lang){
	var temp = string.split('<svg',2);
	var temp2 = splitOnce(temp[1],'>');
	var content = temp2[1];
	content = content.replace(/(\r\n|\n|\r)/gm,"");
	content = content.replace(/\s+/g," ");
	content = content.replace(/v> </g, "><");
	
	content = content.replace(/'/g, "\"")

	
	//$contenu = str_replace("id=\"", "id=\"".$item."-", $contenu);
	//$contenu = str_replace("xlink:href=\"#", "xlink:href=\"#".$item."-", $contenu);
	//$contenu = str_replace("clip-path=\"url(#", "clip-path=\"url(#".$item."-", $contenu);
	
	//var reg = new RegExp('(id=")|(xlink:href="#)|(clip-path="url.#)', "g");
	var reg = new RegExp('(id=")', "g");
	content = content.replace(reg, "$1"+lang+"-");
	reg = new RegExp('(xlink:href="#)', "g");
	content = content.replace(reg, "$1"+lang+"-");
	var reg = new RegExp("\(clip-path=\"url.#\)", "g");
	content = content.replace(reg, "$1"+lang+"-");
	
	var x = 0;
	var y = 0;
	
	var heightTemp = temp2[0].split('height="');
	if(heightTemp[1]){
		var heightTemp2 = splitOnce(heightTemp[1],'"');
		var height = heightTemp2[0];
	}
	
	var widthTemp = temp2[0].split('width="');
	if(widthTemp[1]){
		var widthTemp2 = splitOnce(widthTemp[1],'"');
		var width = widthTemp2[0];
	}
	
	var viewTemp = temp2[0].split('viewBox="');
	if(viewTemp[1]){
		var viewTemp2 = splitOnce(viewTemp[1],'"');
		var view = viewTemp2[0].split(' ');
		
		if(view[0]){
			x = view[0];
		}
		if(view[1]){
			y = view[1];
		}
		if(view[2]){
			width = view[2];
		}
		if(view[3]){
			height = view[3];
		}
	}

	string = '<svg viewBox="'+x+' '+y+' '+width+' '+height+'">'+ content;
	//console.log("clean:"+string);
	return string;
}


